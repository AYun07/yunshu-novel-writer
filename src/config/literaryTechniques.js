// 获取所有技法
export function getAllTechniques() {
  return Object.values(literaryTechniques)
}

// 按分类获取技法
export function getTechniquesByCategory(category) {
  return Object.values(literaryTechniques).filter(t => t.category === category)
}

// 获取技法
export function getTechniqueById(id) {
  return literaryTechniques[id] || null
}

// 获取所有分类
export function getTechniqueCategories() {
  const categories = {}
  Object.values(literaryTechniques).forEach(t => {
    if (!categories[t.category]) {
      categories[t.category] = []
    }
    categories[t.category].push(t)
  })
  return categories
}
