/**
 * 获取所有分类
 * @returns {Object} 分类及其大师列表
 */
export function getMasterCategories() {
  const categories = {}
  Object.values(masterAuthors).forEach(master => {
    if (!categories[master.category]) {
      categories[master.category] = []
    }
    categories[master.category].push(master)
  })
  return categories
}

/**
 * 搜索大师
 * @param {string} keyword - 搜索关键词
 * @returns {Array} 匹配的大师列表
 */
export function searchMasters(keyword) {
  const lowerKeyword = keyword.toLowerCase()
  return Object.values(masterAuthors).filter(master => 
    master.name.toLowerCase().includes(lowerKeyword) ||
    master.description.toLowerCase().includes(lowerKeyword) ||
    master.styleFeatures.some(f => f.toLowerCase().includes(lowerKeyword))
  )
}

