/**
 * 生成创作提示词
 * @param {Object} options - 创作选项
 * @returns {string} 完整的创作提示词
 */
export function generateCreationPrompt(options) {
  const {
    masterId,
    techniqueIds = [],
    theme,
    outline = '',
    wordCount = 2000,
    characters = [],
    worldSettings = []
  } = options

  const master = getMasterById(masterId)
  const techniques = techniqueIds.map(id => getTechniqueById(id)).filter(Boolean)

  let prompt = ''

  // 添加大师风格提示词
  if (master) {
    prompt += `【创作风格】\n${master.systemPrompt}\n\n`
  }

  // 添加叙事技法提示词
  if (techniques.length > 0) {
    prompt += `【叙事技法】\n`
    techniques.forEach(tech => {
      prompt += `${tech.name}：${tech.systemPrompt}\n\n`
    })
  }

  // 添加创作要求
  prompt += `【创作要求】\n`
  prompt += `- 主题：${theme}\n`
  prompt += `- 目标字数：${wordCount}字\n`
  if (outline) {
    prompt += `- 大纲：${outline}\n`
  }
  if (characters.length > 0) {
    prompt += `- 主要人物：${characters.map(c => c.name).join('、')}\n`
  }
  if (worldSettings.length > 0) {
    prompt += `- 世界观：${worldSettings.join('、')}\n`
  }

  prompt += `\n请根据以上要求创作一篇文学作品。`

  return prompt
}

/**
 * 生成润色提示词
 * @param {string} content - 原文内容
 * @param {number} round - 润色轮次
 * @returns {string} 润色提示词
 */
export function generateRefinementPrompt(content, round) {
  const refinement = getRefinementRound(round)
  if (!refinement) return ''

  return `${refinement.systemPrompt}

【原文】
${content}`
}

/**
 * 计算质量评分
 * @param {Object} scores - 各维度评分
 * @returns {number} 总分
 */
export function calculateTotalScore(scores) {
  let total = 0
  qualityDimensions.forEach(dim => {
    const score = scores[dim.id]?.score || 0
    total += score * dim.weight
  })
  return Math.round(total)
}

