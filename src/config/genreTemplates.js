// 获取所有模板
export function getAllGenreTemplates() {
  return Object.values(genreTemplates)
}

// 按分类获取模板
export function getGenresByCategory(category) {
  return Object.values(genreTemplates).filter(g => g.category === category)
}

// 获取模板
export function getGenreById(id) {
  return genreTemplates[id] || null
}

// 获取所有分类
export function getGenreCategories() {
  const categories = {}
  Object.values(genreTemplates).forEach(g => {
    if (!categories[g.category]) {
      categories[g.category] = []
    }
    categories[g.category].push(g)
  })
  return categories
}
