// 获取所有风格
export function getAllStyles() {
  return Object.values(masterStyles)
}

// 按分类获取风格
export function getStylesByCategory(category) {
  return Object.values(masterStyles).filter(style => style.category === category)
}

// 获取风格
export function getStyleById(id) {
  return masterStyles[id] || null
}

// 获取所有分类
export function getCategories() {
  const categories = {}
  Object.values(masterStyles).forEach(style => {
    if (!categories[style.category]) {
      categories[style.category] = []
    }
    categories[style.category].push(style)
  })
  return categories
}
