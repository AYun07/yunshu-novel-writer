// 获取精修流程
export function getRefinementSteps() {
  return Object.values(refinementSystem).sort((a, b) => a.step - b.step)
}

// 获取指定步骤
export function getRefinementStep(id) {
  return refinementSystem[id] || null
}

// 生成精修Prompt
export function generateRefinementPrompt(stepId, params) {
  const step = refinementSystem[stepId]
  if (!step) return ''
  
  let prompt = step.prompt
  Object.keys(params).forEach(key => {
    prompt = prompt.replace(new RegExp(`\\{${key}\\}`, 'g'), params[key])
  })
  
  return prompt
}
